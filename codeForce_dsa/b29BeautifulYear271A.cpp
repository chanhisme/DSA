/*
CF 271A - Beautiful Year

Time limit: 2 seconds
Memory limit: 256 MB

It seems like the year 2013 came only yesterday. A curious fact is that 2013
is the first year after 1987 whose digits are all distinct.

Given a year number y, find the minimum year number that is strictly greater
than y and whose digits are all distinct.

Input:
- A single integer y
    y = year number

Constraints:
- 1000 <= y <= 9000

Output:
- Print a single integer: the smallest year greater than y such that all
  of its digits are distinct.

Example:
Input:
1987

Output:
2013
*/